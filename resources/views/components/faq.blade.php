                <li>With your doctor’s control you can discontinue to use blood-thinning medications such as aspirin at least 1 week before.</li>
                <li>Since herbal teas such as green tea may increase bleeding, before surgery avoid foods and beverages that will cause bleeding. (Ginger, turmeric, garlic, red wine, some kinds of peppers, olive oil)</li>
                <li>Since smoking can affect the healing process negatively, the patient should avoid consumption or use of tobacco and tobacco products.</li>
                <li>1 week before the hair transplant it is forbidden to consume alcohol or drink alcoholic products.</li>
